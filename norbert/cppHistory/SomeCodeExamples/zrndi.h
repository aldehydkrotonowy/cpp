/* ------------------------------ FUNCTION -------------------------- rnd0_I() */
/*Call :  time_t t;                                                           */
/*           int i;                                                           */
/*                                                                            */
/*        srand(time(&t));                                                    */
/*           i = rnd0_I(9);                                                    */
/* -------------------------------------------------------------------------- */
int rnd0_I(
int maxI)
{
int x;

    x = rand() % maxI;
    if(x)
    {
     x *= -pow(-1,x);
    }
 return(x);
}

