void p_mI(
PmF mA)
{
 int i;
 int j;

 for (i = 0 ; i < mA->rows ; i++)
  {
   printf("\n");
   for ( j = 0 ; j < mA->cols; j++,j++)
    {
     printf(" %6.0f",*(mA->pb+i *mA->cols+j));
    }
  }
  printf("\n");
}


